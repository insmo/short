func Decode(buf []byte) int {
	sum := 0

	for i := 0; i < len(buf); i++ {
		c := buf[i]
		sum = sum*base + alphabetLookup[c]
	}

	return sum
}

func Encode(n int) []byte {
	if n == 0 {
		return []byte{alphabet[0]}
	}

	buf := make([]byte, bufSize)
	i := bufSize

	for n > 0 && i >= 0 {
		i--
		buf[i] = alphabet[n%base]
		n /= base
	}

	return buf[i:]
}
